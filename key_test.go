func (s *ClientSuite) TestKeySetSuccess(c *C) {
	ts := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		reqContentType := r.Header.Get("Content-Type")
		c.Assert(reqContentType, Equals, "application/x-www-form-urlencoded")

		body, bodyErr := ioutil.ReadAll(r.Body)
		c.Assert(bodyErr, IsNil)
		c.Assert(string(body), Equals, "data=ssh-ed25519+AAAAC3NzaC1lZDI1NTE5AAAAINLgdTpQpCb9e4vB2PVqEj8ElBuz4QAe4IgmETHi2Kf%2F+test&name=test")

		w.Header().Set("Content-Type", "application/json")
		w.WriteHeader(http.StatusOK)

		pwd, pwdErr := os.Getwd()
		c.Assert(pwdErr, IsNil)

		data, readErr := ioutil.ReadFile(fmt.Sprintf("%s/test/response/key_get.json", pwd))
		c.Assert(readErr, IsNil)

		_, err := w.Write(data)
		c.Assert(err, IsNil)
	}))
	defer ts.Close()

	robotClient := client.NewBasicAuthClient("user", "pass")
	robotClient.SetBaseURL(ts.URL)

	key, err := robotClient.KeySet("test", "ssh-ed25519 AAAAC3NzaC1lZDI1NTE5AAAAINLgdTpQpCb9e4vB2PVqEj8ElBuz4QAe4IgmETHi2Kf/ test")
	c.Assert(err, IsNil)
	c.Assert(key.Fingerprint, Equals, testKey)
}

